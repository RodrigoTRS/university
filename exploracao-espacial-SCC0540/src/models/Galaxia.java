package models;

public class Galaxia {
    String nome;
    String tipo;
    String massaTotal;
    String massaVisivel;

    public Galaxia() {}

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getMassaTotal() {
        return massaTotal;
    }

    public void setMassaTotal(String massaTotal) {
        this.massaTotal = massaTotal;
    }

    public String getMassaVisivel() {
        return massaVisivel;
    }

    public void setMassaVisivel(String massaVisivel) {
        this.massaVisivel = massaVisivel;
    }
}
